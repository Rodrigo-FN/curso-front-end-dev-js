function somar(numero1, numero2) {
    let soma = numero1 + numero2
    return 'A soma dos dois números é: ' + soma
}

document.write(somar(10,5) + '<br>')

function subtração(n1, n2) {
    let subtrair = n1 - n2
    return `A soma dos dois números é: ${subtrair} <br>`
}

document.write(subtração(20,10))

function multiplicação(n1, n2) {
    let multiplicar = n1 * n2
    return `A soma dos dois números é: ${multiplicar} <br>`
}

document.write(multiplicação(20,10))

function divisão(n1, n2) {
    let dividir = n1 / n2
    return `A soma dos dois números é: ${dividir} <br>`
}

document.write(divisão(20,10))